// Creates a "CRILOS Start" shortcut on the user's Desktop pointing at the
// launcher. Wired as an npm "postinstall" (runs automatically after every
// `npm install`) and as `npm run shortcut` (manual re-create).
//
// Best-effort by design: everything is wrapped so it can NEVER fail the install
// (a missing Desktop, no PowerShell, a locked-down box, etc. just skips it).
// Idempotent (overwrites an existing shortcut). Set CRILOS_NO_SHORTCUT=1 to skip.

import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs";
import os from "node:os";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const NAME = "CRILOS Start";

try {
  if (process.env.CRILOS_NO_SHORTCUT) {
    process.exit(0);
  }

  if (process.platform === "win32") {
    // Real .lnk via WScript.Shell. SpecialFolders('Desktop') resolves a
    // OneDrive-redirected Desktop; icon comes from the app's favicon.
    const q = (s) => s.replace(/'/g, "''"); // escape single quotes for PowerShell
    const ps = [
      "$W = New-Object -ComObject WScript.Shell",
      "$d = $W.SpecialFolders('Desktop')",
      `$s = $W.CreateShortcut((Join-Path $d '${NAME}.lnk'))`,
      `$s.TargetPath = '${q(path.join(repoRoot, "start.cmd"))}'`,
      `$s.WorkingDirectory = '${q(repoRoot)}'`,
      `$s.IconLocation = '${q(path.join(repoRoot, "src", "app", "favicon.ico"))},0'`,
      "$s.Description = 'Launch the CRILOS Dashboard'",
      "$s.Save()",
    ].join("; ");
    execFileSync("powershell", ["-NoProfile", "-NonInteractive", "-Command", ps], {
      stdio: "ignore",
    });
    console.log(`CRILOS: desktop shortcut created (${NAME}).`);
  } else if (process.platform === "darwin") {
    // No .lnk on macOS; write a tiny wrapper .command that cd's to the repo and
    // execs the real launcher. Avoids editing start.command, needs no AppleScript
    // / automation permission, and isn't Gatekeeper-quarantined (locally written).
    const shortcut = path.join(os.homedir(), "Desktop", `${NAME}.command`);
    fs.writeFileSync(
      shortcut,
      `#!/usr/bin/env bash\n# Auto-generated by CRILOS install.\ncd ${JSON.stringify(
        repoRoot,
      )} && exec ./start.command\n`,
    );
    fs.chmodSync(shortcut, 0o755);
    console.log(`CRILOS: desktop shortcut created (${NAME}).`);
  }
  // Other platforms (Linux, etc.): silently skip.
} catch {
  // Best-effort only — never break `npm install`. The launcher scripts in the
  // repo root still work directly.
}

process.exit(0);
